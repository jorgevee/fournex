def test_prompt_launch_bound_question_mentions_compile_or_graphs():
    result = build_telemetry_explain_result(scope_data=_launch_scope())
    if result["primary_bottleneck"] == "launch_bound":
        txt = render_training_llm_prompt_txt(result)
        question_section = txt.split("SPECIFIC QUESTION")[1]
        assert "compile" in question_section.lower() or "graph" in question_section.lower()


def test_prompt_shows_fat_section_when_high():
    result = build_telemetry_explain_result(scope_data=_launch_scope())
    fat = result.get("framework_abstraction_tax")
    if fat and fat.get("score", 0) >= 20:
        txt = render_training_llm_prompt_txt(result)
        assert "FRAMEWORK ABSTRACTION TAX" in txt


def test_prompt_secondary_excluded_from_secondary_section():
    result = build_telemetry_explain_result(scope_data=_launch_scope())
    txt = render_training_llm_prompt_txt(result)
    # If SECONDARY ISSUES present, primary should not appear there
    if "SECONDARY ISSUES" in txt:
        primary = result["primary_bottleneck"] or ""
        secondary_block = txt.split("SECONDARY ISSUES")[1].split("FRAMEWORK")[0]
        assert primary.replace("_", " ") not in secondary_block


