"""Tests for the training-telemetry explain path (frx_telemetry_explain_v0)."""
from __future__ import annotations

import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(ROOT / "python"))
sys.path.insert(0, str(ROOT / "tests" / "python"))

import fournex as fn
from fournex.analysis import summarize_step_scope
from fournex.explain import (
    build_telemetry_explain_result,
    render_training_summary_txt,
    render_training_llm_prompt_txt,
    render_evidence_json,
    _select_scope_data,
)
from analysis_bottleneck_golden_cases import (
    LAUNCH_BOUND_TINY_KERNEL_EVENTS,
    INPUT_BOUND_EVENTS,
)


# ── Fixtures ──────────────────────────────────────────────────────────────────

def _launch_scope():
    return summarize_step_scope(LAUNCH_BOUND_TINY_KERNEL_EVENTS)

def _input_scope():
    return summarize_step_scope(INPUT_BOUND_EVENTS)


# ── build_telemetry_explain_result ────────────────────────────────────────────

def test_result_schema():
    result = build_telemetry_explain_result(scope_data=_launch_scope())
    assert result["schema"] == "frx_telemetry_explain_v0"


def test_result_has_required_keys():
    result = build_telemetry_explain_result(scope_data=_launch_scope())
    for key in ("schema", "scope_name", "step_count", "primary_bottleneck",
                "secondary_bottlenecks", "confidence", "why", "key_metrics",
                "phase_fractions", "top_recommendations", "framework_abstraction_tax",
                "missing_data", "bottleneck_evidence"):
        assert key in result, f"missing key: {key}"


def test_launch_bound_primary_bottleneck():
    result = build_telemetry_explain_result(scope_data=_launch_scope())
    assert result["primary_bottleneck"] is not None


def test_input_bound_primary_bottleneck():
    result = build_telemetry_explain_result(scope_data=_input_scope())
    assert result["primary_bottleneck"] is not None


def test_key_metrics_contains_gpu_utilization():
    result = build_telemetry_explain_result(scope_data=_launch_scope())
    assert "average_gpu_utilization_pct" in result["key_metrics"]


def test_framework_abstraction_tax_present_for_launch_bound():
    result = build_telemetry_explain_result(scope_data=_launch_scope())
    fat = result.get("framework_abstraction_tax")
    assert fat is not None
    assert fat["score"] > 0


def test_top_recommendations_list():
    result = build_telemetry_explain_result(scope_data=_launch_scope())
    assert isinstance(result["top_recommendations"], list)
    for r in result["top_recommendations"]:
        assert "title" in r
        assert "priority" in r


def test_secondary_bottlenecks_exclude_primary():
    result = build_telemetry_explain_result(scope_data=_launch_scope())
    primary = result["primary_bottleneck"]
    assert primary not in result["secondary_bottlenecks"]


def test_phase_fractions_input_bound():
    result = build_telemetry_explain_result(scope_data=_input_scope())
    # input_bound evidence should populate input_bound fraction
    if result["primary_bottleneck"] == "input_bound":
        assert "input_bound" in result["phase_fractions"]
        assert result["phase_fractions"]["input_bound"] > 0


